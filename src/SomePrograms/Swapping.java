package SomePrograms;

public class Swapping {

	public static void main(String[] args) {
		int a=10, b=5, c;
		
		System.out.println("Brfore Swapping: ");
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		
		//Swapping
		c=a;
		a=b;
		b=c;
		
		System.out.println("\nAfter Swapping: ");
		System.out.println("a = " + a);
		System.out.println("b = " + b);

	}

}
