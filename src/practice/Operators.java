package practice;

public class Operators {

	public static void main(String[] args) {
		
		int a=20;
		int b=10;
		
		//Arithmetic Operator
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		//Relational Operator
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a <= b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		//Logical Operator
		boolean x=true;
		boolean y=false;
		
		System.out.println(x && y);
		System.out.println(x || y);
		System.out.println(!x);
		System.out.println(!y);
		
	}

}
